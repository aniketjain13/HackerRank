void staircase(int n) {
    for(int i = 0; i < n; i++){
        for(int j = n-i-1; j > 0; j--)
            cout <<" ";
        for(int j = n-1-i; j < n; j++)
            cout<< "#";
        cout<<"\n";
    }
}
